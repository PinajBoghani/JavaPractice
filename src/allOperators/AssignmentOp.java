package allOperators;

public class AssignmentOp {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        a+=30;
        // a=a+30
        // 40=10+30
        System.out.println(a);
        b-=a;
        //b=b-a
        //-20=20-40
        System.out.println(b);




    }
}