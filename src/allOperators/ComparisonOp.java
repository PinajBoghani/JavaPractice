package allOperators;

public class ComparisonOp {
    public static void main(String[] args) {


        int engMarks = 86;
        int mathsMarks = 97;
        int scienceMarks = 86;

        System.out.println(engMarks<mathsMarks);
        System.out.println(scienceMarks<=engMarks);
        System.out.println(mathsMarks<=scienceMarks);
        System.out.println(engMarks==scienceMarks);
        System.out.println(engMarks!=mathsMarks);



    }


}
