package variablesAndDatatypes;

public class ReassignValue {
    public static void main(String[] args) {

        char size='s';
        int age=10;
        System.out.println("my age is "+age+ " my t-shirt size is "+size);

         age=16;
         size='m';
        System.out.println("now my age is "+age+" and my tshirt size is " +size);




    }
}
